#if defined(__amd64) || defined(__x86_64)
	push	%rbp
	movq	%rsp, %rbp
	subq	$0x30, %rsp 

	push	%rbx

	mov	%rdx, %r8
	mov	%rsi, %rdx
	mov	%rdi, %rcx

	/* 1) Align */
	mov	%rcx, %rdi	/* rdi = *buf */
	neg	%rcx
	and	$7, %rcx	/* calculate the unalignment amount of the address */

	je	proc_block	/* Skip if aligned */

	/* Calculate CRC of unaligned bytes of the buffer (if any) */
	mov	(%rdi), %rbx	/* load a quadword from the buffer */
	add	%rcx, %rdi	/* align buffer pointer for quadword processing */
	sub	%rcx, %rdx	/* update buffer length */

	jae	align_loop
	add	%rcx, %rdx

little_unalign:
	/* crc32b %bl,%r8d */
	.byte 0xf2, 0x44, 0x0f, 0x38, 0xf0, 0xc3 
	shr	$8, %rbx
	dec	%rdx
	jne	little_unalign
	jmp	do_return 

align_loop:
	/* crc32b %bl,%r8d */
	.byte 0xf2, 0x44, 0x0f, 0x38, 0xf0, 0xc3 
	shr	$8, %rbx	/* get next byte */
	dec	%rcx
	jne	align_loop

	/* 2) Process blocks */
proc_block:
	/* compute num of bytes to be processed */
	mov	%rdx, %rbx	/* save number of bytes in rbx */
	cmp	$128*24, %rdx
	jae	full_block

continue_block:
	/* rdx < 128*24 */
	mov	$2731, %rax	/* 2731 = ceil(2^16/24) */
	mul	%edx
	shr	$16, %rax

	/* eax contains floor(bytes/24) = num 24-byte chunks to do
	 * process rax 24-byte chunks (128 >= rax >= 0) */

	/*
	  compute end address of each block
	  rdi -> block 0 (base addr + RAX * 8)
	  rsi -> block 1 (base addr + RAX * 16)
	  r11 -> block 2 (base addr + RAX * 24)
	*/
	lea	(%rdi, %rax, 8), %rdi
	lea	(%rdi, %rax, 8), %rsi
	lea	(%rsi, %rax, 8), %r11

	xor	%r9, %r9
	xor	%r10, %r10

	/* branch into array */
	lea	jump_table, %rcx
	movzwq	(%rcx, %rax, 2), %rdx	/* rdx is offset from crc_array */
	lea	crc_array(%rdx), %rcx
	jmp	*%rcx

	/* 2a) Process full blocks*/
full_block:
	mov	$128, %rax
	lea	128*8*2(%rdi), %rsi
	lea	128*8*3(%rdi), %r11
	add	$128*8*1, %rdi

	xor	%r9, %r9
	xor	%r10, %r10

	/* branch into array */
	jmp	crc_128

	/* 3) CRC array */
