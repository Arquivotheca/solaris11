#define	INVALID                \
{                              \
	.in_type = INST_NONE,  \
	.in_arch = 0,          \
	.in_data = {           \
		.in_tbl = NULL \
	}                      \
}

#define	INVALIDA(_arch)        \
{                              \
	.in_type = INST_NONE,  \
	.in_arch = _arch,      \
	.in_data = {           \
		.in_tbl = NULL \
	}                      \
}

#define	INST(_name, _arch, _flags)           \
{                                            \
	.in_type = INST_DEF,	             \
	.in_arch = _arch,		     \
	.in_data = {			     \
		.in_def = {		     \
			.in_name   = _name,  \
			.in_flags  = _flags  \
		}			     \
	}				     \
}

#define	TABLE(_name, _arch)      \
{                                \
	.in_type = INST_TBL,     \
	.in_arch = _arch,        \
	.in_data = {             \
		.in_tbl = &_name \
	}                       \
}

#define	OVERLAY(_idx, _inst) \
{                            \
	.ov_idx  = _idx,     \
	.ov_inst = _inst     \
}

#define	OVERLAY_END { .ov_idx = -1, .ov_inst = INVALID }

