#include <private.h>

#undef erasewchar

int
erasewchar(wchar_t *wcp)
{
	int code;

	code = __m_tty_wc(VERASE, wcp);

	return (code);
}

#undef killwchar

int
killwchar(wchar_t *wcp)
{
	int code;

	code = __m_tty_wc(VKILL, wcp);

	return (code);
}
