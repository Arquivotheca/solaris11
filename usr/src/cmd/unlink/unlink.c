#include <stdio.h>
#include <strings.h>
#include <stdlib.h>
#include <unistd.h>
#include <libintl.h>
#include <locale.h>

int
main(int argc, char **argv)
{
	int res = 0;

	(void) setlocale(LC_ALL, "");

#if !defined(TEXT_DOMAIN)
#define	TEXT_DOMAIN "SYS_TEST"
#endif
	(void) textdomain(TEXT_DOMAIN);

	if (argc != 2) {
		char *p = gettext("Usage: /usr/sbin/unlink name\n");

		(void) write(STDERR_FILENO, p, strlen(p));
		exit(1);
	}

	if (res = unlink(argv[1]))
		perror("unlink");

	return (res);
}
